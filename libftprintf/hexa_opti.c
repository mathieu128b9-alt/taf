#include <unistd.h>

void	ft_putnbr_base(long nb)
{
	char	*base;

	base = "0123456789abcdef";
	if (nb < 0)
	{
		write (1, "-", 1);
		nb = -nb;
	}
	if (nb >= 16)
		ft_putnbr_base(nb / 16);
	write(1, &base[nb % 16], 1);
}
