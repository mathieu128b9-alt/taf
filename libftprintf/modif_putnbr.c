#include "ft_printf.h"

int	modif_putnbr(unsigned long n)
{
	int	i;

	i = 0;
	if (n >= 10)
	{
		i = modif_putnbr(n / 10);
	}
	ft_putchar((n % 10) + '0');
	i++;
	return (i);
}
