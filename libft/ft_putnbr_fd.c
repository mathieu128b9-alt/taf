#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
	{
		write (fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write (fd, '-', 1);
		n = -n;
	}
	while (n >= 10)
	{
		ft_putnbr_fd(n / 10);
	}
	ft_putchar_fd((n % 10) + '0', fd);
}
